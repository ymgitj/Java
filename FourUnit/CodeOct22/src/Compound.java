/*
   复和语句
*/
public class Compound
{
    public static void main(String args[])
    {
        int y=40;
        System.out.println("y 的值为:"+y);
        int z=245;
        boolean b;
        //复和语句
        {
            b=y>z;
            System.out.println("y>z成立吗"+b);
            
        }
        
    }
}
