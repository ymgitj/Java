class Compare
{
    public static void main(String args[])
    {
        int num1=10;
        int num2=20;
        System.out.println("10<20....." + (num1 < num2));
        System.out.println("10>20....." + (num1 > num2));
        System.out.println("10<=20...." + (num1 <= num2));
        System.out.println("10>=20...." + (num1 >= num2));
        System.out.println("10!=20...." + (num1 != num2));
        System.out.println("10==20...." + (num1 == num2));
    }
}
